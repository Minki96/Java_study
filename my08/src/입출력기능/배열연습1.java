package 입출력기능;

public class 배열연습1 {
	public static void main(String[] args) {
		int[] num = new int[10];
		// int[] : int로 만들어진 배열타입
		// num : 배열의 주소가 들어있음, 참조형
		// new : 복사
		// new int : int변수를 복사해
		// new int[] : int변수를 10개 복사해n
		
		System.out.println(num);
		System.out.println(num[0]);
		//배열은 변수에 초기값을 넣어준다.
		//int배열은 모두 다 으로 초기화ㅔ


	}
}
